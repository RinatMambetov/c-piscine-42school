int	ft_str_is_alpha(char *str)
{
	int	i;
	int	AZ;
	int	az;

	i = 0;
	AZ = 0;
	az = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'A' && str[i] <= 'Z'))
			AZ = 1;
		if ((str[i] >= 'a' && str[i] <= 'z'))
			az = 1;
		if ((AZ == 0) && (az == 0))
			return (0);
		i++;
		AZ = 0;
		az = 0;
	}
	return (1);
}
